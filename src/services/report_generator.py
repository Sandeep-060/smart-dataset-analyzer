styles = getSampleStyleSheet()

TITLE_STYLE = ParagraphStyle(
    "Title",
    parent=styles["Heading1"],
    alignment=TA_CENTER,
    fontSize=22,
    leading=28,
    spaceAfter=18,
)

SECTION_STYLE = ParagraphStyle(
    "Section",
    parent=styles["Heading2"],
    fontSize=15,
    textColor=colors.darkblue,
    spaceBefore=10,
    spaceAfter=8,
)

NORMAL_STYLE = styles["BodyText"]

SMALL_STYLE = ParagraphStyle(
    "Small",
    parent=styles["BodyText"],
    fontSize=9,
    leading=12,
)


def divider(elements):

    table = Table([[""]], colWidths=[7.2 * inch])

    table.setStyle(
        TableStyle([
            ("LINEBELOW", (0, 0), (-1, -1), 0.5, colors.grey)
        ])
    )

    elements.append(table)
    elements.append(Spacer(1, 0.15 * inch))



def add_heading(elements, title):

    elements.append(
        Paragraph(title, SECTION_STYLE)
    )

    elements.append(
        Spacer(1, 0.08 * inch)
    )

def dataframe_to_table(dataframe):

    data = [list(dataframe.columns)]

    data.extend(dataframe.values.tolist())

    table = Table(data)

    table.setStyle(
        TableStyle(

            [

                ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#1f77b4")),

                ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),

                ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),

                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),

                ("BOTTOMPADDING", (0, 0), (-1, 0), 8),

                ("TOPPADDING", (0, 0), (-1, -1), 6),

                ("BOTTOMPADDING", (0, 1), (-1, -1), 6),

                ("BACKGROUND", (0, 1), (-1, -1), colors.beige),

                ("FONTSIZE", (0, 0), (-1, -1), 9),

            ]

        )
    )

    return table

def metric_table(dictionary):

    rows = [["Metric", "Value"]]

    for key, value in dictionary.items():

        rows.append(
            [str(key), str(value)]
        )

    table = Table(
        rows,
        colWidths=[3 * inch, 3 * inch]
    )

    table.setStyle(

        TableStyle(

            [

                ("BACKGROUND", (0, 0), (-1, 0), colors.darkblue),

                ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),

                ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),

                ("BACKGROUND", (0, 1), (-1, -1), colors.whitesmoke),

                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),

                ("BOTTOMPADDING", (0, 0), (-1, 0), 8),

                ("TOPPADDING", (0, 1), (-1, -1), 5),

                ("BOTTOMPADDING", (0, 1), (-1, -1), 5),

                ("FONTSIZE", (0, 0), (-1, -1), 9),

            ]

        )

    )

    return table

